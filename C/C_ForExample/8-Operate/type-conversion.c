#include <stdio.h>


int main(){
	
	printf("整型输出：%d\n",1 + 2.0); //会自动转成浮点型数据 
	printf("浮点型输出：%f\n",1 + 2.0); 
	printf("|一个浮点型的数用整型输出就会有错误|\n\n");
	
	printf("1+2.0整型输出：%d\n",1 + (int)2.0);  //会强制转换成整型数据 
	printf("1+1.9整型输出：%d\n",1 + (int)1.9);   //会将1.9转换成1，而不是四舍五入!!!!! 
	printf("浮点型强制用整型输出：%d\n",(int)(1.0 + 2.0)); 
	 
	return 0;
}
